class GeneratedLevelConfig {
  const GeneratedLevelConfig({
    required this.levelNumber,
    required this.templateId,
    required this.difficultyTier,
    required this.targetScore,
    required this.moves,
    required this.boardRows,
    required this.boardColumns,
    required this.tileVariantCount,
    required this.obstacleCount,
    required this.rewardCoin,
  });

  final int levelNumber;
  final int templateId;
  final int difficultyTier;
  final int targetScore;
  final int moves;
  final int boardRows;
  final int boardColumns;
  final int tileVariantCount;
  final int obstacleCount;
  final int rewardCoin;
}
